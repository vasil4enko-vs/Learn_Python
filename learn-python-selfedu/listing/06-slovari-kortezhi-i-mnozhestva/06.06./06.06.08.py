"""6.6. Генераторы множеств и генераторы словарей.

Пример использования генератора словарей с условием
"""  # noqa: RUF002

m = {
    "безнадежно": 0,
    "убого": 1,
    "неудовл.": 2,
    "удовл.": 3,
    "хорошо": "4",
    "отлично": "5",
}

a = {int(value): key for key, value in m.items() if 2 <= int(value) <= 5}  # noqa: PLR2004

print(a)
